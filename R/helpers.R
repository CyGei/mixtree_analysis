# Relevant functions to produce the true transmission tree.

print.params <- function(x, ...) {
  cat("-------------------\n")
  cat("Epidemic Parameters\n")
  cat("-------------------\n")

  # Offspring distribution
  cat("Distributions:\n")
  cat(" Offspring:\n")
  cat("  name:", x$offspring_dist$name, "\n")
  cat("  parameters:\n")
  cat("   k (size):", x$offspring_dist$parameters$k, "\n")
  cat("   R (mu):", x$offspring_dist$parameters$R, "\n\n")

  # Generation time distribution
  cat(" Generation time:\n")
  cat("  name:", x$generation_time$name, "\n")
  cat("  parameters:\n")
  cat("   shape:", x$generation_time$parameters$shape, "\n")
  cat("   scale:", x$generation_time$parameters$scale, "\n")
  cat("   mu:", x$generation_time$parameters$mu, "\n")
  cat("   sd:", x$generation_time$parameters$sd, "\n")
  cat("   cv:", x$generation_time$parameters$cv, "\n\n")

  # Other parameters
  cat("Simulation settings:\n")
  cat(" epidemic size:", x$epidemic_size, "\n")
  cat(" simulation duration:", x$duration, "\n")
  cat(" replicates:", x$replicates, "\n")

  invisible(x)
}

#' Build Epidemic Parameters
#'
#' Constructs a list of parameters for simulating the true transmission tree.
#'
#' @param off_R Numeric. Mean of the negative binomial offspring distribution.
#' @param off_k Numeric. Dispersion parameter of the negative binomial offspring distribution.
#' @param gt_mu Numeric. Mean generation time (gamma distribution).
#' @param gt_sd Numeric. Standard deviation of generation time (gamma distribution).
#' @param epidemic_size Integer. The epidemic's final size (number of cases).
#' @param duration Integer. Duration of the simulation (in days).
#' @param replicates Integer. Nnumber of trees to simulate.
#'
#' @return An object of class \code{"params"} containing simulation settings.
build_params <- function(
  off_R,
  off_k,
  gt_mu,
  gt_sd,
  epidemic_size,
  duration,
  replicates
) {
  offspring_dist <- list(
    name = "nbinom",
    parameters = list(k = off_k, R = off_R),
    r = function(n = 1) rnbinom(n = n, size = off_k, mu = off_R),
    d = function(x) dnbinom(x, size = off_k, mu = off_R)
  )

  gt_cv <- gt_sd / gt_mu
  gt_params <- epitrix::gamma_mucv2shapescale(mu = gt_mu, cv = gt_cv)
  generation_time <- distcrete::distcrete(
    name = "gamma",
    shape = gt_params$shape,
    scale = gt_params$scale,
    interval = 1
  )
  generation_time$parameters <- list(
    mu = gt_mu,
    sd = gt_sd,
    cv = gt_cv,
    shape = gt_params$shape,
    scale = gt_params$scale
  )

  params <- list(
    offspring_dist = offspring_dist,
    generation_time = generation_time,
    epidemic_size = epidemic_size,
    duration = duration,
    replicates = replicates
  )
  class(params) <- "params"

  return(params)
}


# -------------------------------------------------
# True transmission tree
# -------------------------------------------------
#' Relabel Tree IDs
#' Replaces original case IDs with sequential integers starting from 1, ordered by infection date and ID.
#' @param tree A tibble with columns \code{from}, \code{to}, and \code{date}.
#' @return A tibble with the same structure as \code{tree} but with relabeled IDs.
#' @keywords internal
relabel_tree <- function(tree) {
  id_map <- tree |>
    arrange(date, to) |>
    pull(to) |>
    (\(id) setNames(object = as.character(seq_along(id)), nm = id))()

  tree <- tree |>
    mutate(
      from = unname(id_map[from]),
      to = unname(id_map[to])
    )
  return(tree)
}


#' Simulate a True Transmission Tree
#'
#' Simulates a single outbreak using specified epidemic parameters until exactly epidemic_size is reached -> THROUGH TRUNCATION.!!!
#'
#' @param params List of epidemic parameters generated by \code{build_params()}.
#'
#' @return A tibble with columns \code{from}, \code{to}, and \code{date}, representing transmission events.
#'
build_tree <- function(params) {
  repeat_tree <- function(rep_id) {
    repeat {
      out <- simulacr::simulate_outbreak(
        duration = params$duration,
        population_size = 1e4,
        R_values = params$offspring_dist$r(params$epidemic_size),
        dist_generation_time = params$generation_time$d(0:params$duration),
        dist_incubation = 1L
      )$data

      # Check if the outbreak has reached at least epidemic_size
      size_OK <- nrow(out) >= params$epidemic_size
      if (size_OK) break
    }

    tree <- out |>
      slice(1:params$epidemic_size) |> # Truncate to exactly epidemic_size
      transmute(
        from = source,
        to = id,
        date = date_infection
      ) |>
      relabel_tree() |>
      as_tibble()

    mixtree::validate_tree(tree |> slice(-1))

    return(tree)
  }

  lapply(X = seq_len(params$replicates), FUN = repeat_tree)
}

# -------------------------------------------------
# Epidemic forest
# -------------------------------------------------
#' Simulate an Epidemic Forest
#'
#' Generates multiple plausible transmission trees given \code{tree} and \code{params}.
#'
#' @param tree A tibble representing the reference tree (from \code{build_tree()}).
#' @param params List of epidemic parameters generated by \code{build_params()}.
#' @param forest_size Integer. The total number of transmission trees to generate. Default is 200.
#'
#' @return A matrix of ancestries. Where each row corresponds to a tree and each column to a case (excluding the root).
#' For example, entry [i, j] indicates the infector of case j in tree i.
build_forest <- function(tree, params, forest_size = 200L) {
  simulate_tree <- function() {
    n_cases <- length(tree$to)

    # Sample offspring for all cases
    R <- params$offspring_dist$r(n_cases)

    # Ensure first case has R >= 1
    if (R[1] < 1) {
      R[1] <- LaplacesDemon::rtrunc(
        n = 1,
        spec = params$offspring_dist$name,
        a = 0,
        size = params$offspring_dist$parameters$k,
        mu = params$offspring_dist$parameters$R
      )
    }
    # Initialise tree
    sim_tree <- tibble(
      j = NA_character_, # infector
      i = tree$to, # infectee
      date = tree$date, # infectee's date is fixed from reference tree
      R = R # sampled offspring
    )

    generation_time_pmf <- params$generation_time$d(0:params$duration)

    for (row in 2:nrow(sim_tree)) {
      t_i <- sim_tree$date[row]

      j_candidates <- sim_tree |>
        filter(date < t_i) |>
        mutate(foi = R * generation_time_pmf[t_i - date + 1])

      j_chosen <- sample(
        j_candidates$i,
        1,
        prob = j_candidates$foi / sum(j_candidates$foi)
      )
      sim_tree$j[row] <- j_chosen
    }

    stopifnot(sum(is.na(sim_tree$j)) == 1) # one intro only

    sim_tree <- sim_tree |>
      slice(-1) |>
      rename(from = j, to = i)

    mixtree::validate_tree(sim_tree)

    # Return vector of ancestries to save space (i is always 1:n)
    ancestries <- as.vector(sim_tree$from)
    return(ancestries)
  }
  # Rows = forest ID, Columns = case ID
  forest <- t(replicate(forest_size, simulate_tree(), simplify = TRUE))
  colnames(forest) <- tree$to[-1] # Exclude root case
  return(forest)
}

# =================================================
# mixtree test wrapper
# =================================================
# Convert matrix to list of trees
as_forest <- function(forest) {
  col_names <- colnames(forest)
  lapply(seq_len(nrow(forest)), function(i) {
    data.frame(
      from = forest[i, ],
      to = col_names,
      stringsAsFactors = FALSE
    )
  })
}

# Run one test given sampled matrices
.mixtree_test <- function(sample_A, sample_B, method) {
  forest_A <- as_forest(sample_A)
  forest_B <- as_forest(sample_B)

  p_value <- switch(
    method,
    "permanova" = {
      test <- mixtree::tree_test(
        forest_A,
        forest_B,
        method = method,
        test_args = list(permutations = 999)
      )
      test[["Pr(>F)"]][[1]]
    },
    "chisq" = {
      test <- mixtree::tree_test(
        forest_A,
        forest_B,
        method = method,
        test_args = list(simulate.p.value = TRUE, B = 999)
      )
      test[["p.value"]]
    },
    stop("Unknown method: ", method)
  )
  return(p_value)
}

# Run all tests for one pair of forests
mixtree_test <- function(
  forest_id_A,
  forest_id_B,
  forests, # named list: forests[["forest_id"]] = matrix
  forest_sizes, # vector of integers
  methods # vector of characters ("chisq", "permanova")
) {
  forest_A <- forests[[forest_id_A]]
  forest_B <- forests[[forest_id_B]]

  grid <- expand_grid(
    forest_size = forest_sizes,
    method = methods
  )

  results <- purrr::pmap_dfr(grid, function(forest_size, method) {
    sample_A <- forest_A[sample(nrow(forest_A), forest_size), , drop = FALSE]
    sample_B <- forest_B[sample(nrow(forest_B), forest_size), , drop = FALSE]
    p_value <- suppressWarnings(.mixtree_test(sample_A, sample_B, method))

    data.frame(
      forest_id_A = forest_id_A,
      forest_id_B = forest_id_B,
      forest_size = forest_size,
      method = method,
      p_value = p_value
    )
  })

  return(results)
}


